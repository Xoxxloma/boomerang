  '• "sinceDays" — относительный период в днях: «неделя»→7, «две недели»→14, «вчера»→1, «месяц»→30, ' +
  '«год»→365. Нет периода → null.\n' +
  '• "query" — ТОЛЬКО смысловая тема: исходный запрос, из которого УБРАНЫ слова про вид материала, ' +
  'время и служебные обороты («были ли», «какие», «покажи», «я отправлял», «есть ли»). Если после ' +
  'удаления осталась лишь фильтрация без темы — пустая строка "".\n' +
  '• "expansions" — 2–6 канонических терминов, синонимов, расшифровок аббревиатур и переводов для темы, ' +
  'чтобы вытащить записи, где она названа иначе. Используй мировое знание: «контра»/«кс»/«контр-страйк» → ' +
  '["Counter-Strike","CS2","киберспорт"]; «нейронки» → ["нейросети","ИИ","AI"]; «тачки» → ["автомобили","машины"]. ' +
  'Если тема и так однозначна — [].\n' +
  '• "categories" — какие из ПЕРЕЧИСЛЕННЫХ ниже категорий пользователя относятся к запросу. Бери имена ' +
  'СТРОГО из списка, дословно. Если ни одна не подходит или список пуст — [].\n' +
  'Примеры:\n' +
  '«были ли какие-нибудь документы за последнюю неделю?» → {"query":"","types":["document"],"sinceDays":7,"expansions":[],"categories":[]}\n' +
  '«новости контры за последнее время» (категории: Киберспорт, Рецепты) → {"query":"новости Counter-Strike","types":[],"sinceDays":null,"expansions":["Counter-Strike","CS2","киберспорт"],"categories":["Киберспорт"]}\n' +
  '«покажи картинки про котов» → {"query":"коты","types":["image"],"sinceDays":null,"expansions":["кошки","коты"],"categories":[]}\n' +
  '«ипотека за неделю» → {"query":"ипотека","types":[],"sinceDays":7,"expansions":["ипотека","жильё в кредит"],"categories":[]}\n' +
  'Без пояснений, только JSON.';

export function parseQueryPrompt(query: string, categories: string[]): string {
  const cats = categories.length ? categories.join(', ') : '(нет)';
  return `Категории пользователя: ${cats}\n\nЗапрос:\n"""\n${query}\n"""\n\nРазбери его на фильтры, тему, синонимы и категории.`;
}

export const SYNTHESIZE_SYSTEM =
  'Ты — «второй мозг» пользователя. На входе его сохранённые материалы (фрагменты) и тема/вопрос. ' +
  'Собери СТРУКТУРНУЮ фактологическую сводку на русском СТРОГО по этим материалам — список коротких ' +
  'тезисов: один факт в строке, каждая строка начинается с «• » и заканчивается ссылкой на источник ' +
  '[n] (можно несколько: [1][3]). Только факты, конкретика, имена и цифры ИЗ материалов. Без оценок и ' +
  'эпитетов, без обращений к пользователю («ты увлечён», «отличная находка»), без вводных и воды. ' +
  'Каждый тезис обязан иметь хотя бы одну ссылку [n]. Ничего не выдумывай сверх материалов. ' +
  'Цитируй [n] ТОЛЬКО для материалов, реально относящихся к теме/вопросу: материал, попавший в ' +
  'список, но не по теме, — просто игнорируй (не цитируй и не выдумывай под него тезис). ' +
  'Если материалов мало или они не по теме — скажи это одной строкой.';

export function synthesizePrompt(question: string, sources: string): string {
  return `Тема/вопрос: ${question}\n\nСохранённые материалы:\n${sources}\n\nСобери тезисную сводку: «• факт … [n]» по строке на факт.`;
}
