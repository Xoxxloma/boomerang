/** Шаблоны промптов. Держим в одном месте, чтобы тон/формулировки были согласованы. */

/** Правило заголовка — суть записи одной строкой. Один источник истины для title/title+reminder/vision. */
const TITLE_RULE =
  'title — суть записи своими словами одной строкой до 70 символов, на языке оригинала записи, ' +
  'без кавычек и точки в конце (напр. «Идея: оплата подписки через бота»).';

/** Правило детекта напоминания. Один источник истины для remind/title+remind. */
const REMINDER_RULE =
  'Поле reminder — НЕ null ТОЛЬКО если пользователь ЯВНО просит напомнить/вернуть к этому ко времени ' +
  '(«напомни…», «не забыть…», «верни завтра…», «через 2 часа», «в пятницу позвонить»). Простое ' +
  'упоминание даты/времени В СОДЕРЖАНИИ (пересланная новость, цитата, «вчера был на концерте», ' +
  '«встреча в 2010 году») — это НЕ напоминание: reminder=null. Сомневаешься — null. ' +
  'whenIso — абсолютный момент в будущем, ISO8601 со смещением пояса пользователя, посчитанный ' +
  'относительно «сейчас» (дано в запросе). Если час не назван явно («завтра», «в понедельник») — ' +
  'ставь дефолтный час (дан в запросе), минуты 00.';

/**
 * Заголовок ОДНИМ вызовом — для голосовых/видео после транскрипции (L2): у них нет своего названия,
 * без заголовка запись в выдаче — пустышка. Категорий больше нет (организация по источнику, поиск по
 * вектору) — генерируем только title.
 */
export const TITLE_SYSTEM =
  'Ты придумываешь короткий заголовок сохранённой пользователем записи для его «второго мозга». ' +
  'Контент может быть на любом языке. ' +
  TITLE_RULE +
  ' Отвечай строго JSON-объектом вида {"title":"<одна строка>"}.';

export function titlePrompt(signal: string): string {
  return `Сигналы о контенте:\n"""\n${signal}\n"""\n\nПридумай короткий заголовок.`;
}

/**
 * Детект «это инструкция-напоминание + когда» — на живом одиночном сообщении (гейт detectReminder),
 * не при импорте. Категории не определяем — только напоминание.
 */
export const REMIND_SYSTEM =
  'Ты определяешь, просит ли пользователь напомнить ему о сохранённой записи и на какое время. ' +
  'Отвечай строго JSON-объектом вида {"reminder":null|{"whenIso":"<ISO8601 со смещением пояса>"}}. ' +
  REMINDER_RULE;

/** Сигнал + «сейчас» в поясе юзера + дефолтный час — чтобы LLM посчитал абсолютный whenIso. */
export function remindPrompt(signal: string, nowIso: string, defaultHour: number): string {
  return (
    `Сейчас (в поясе пользователя): ${nowIso}. Дефолтный час напоминания, если время не указано: ${defaultHour}:00.\n\n` +
    `Сигналы о контенте:\n"""\n${signal}\n"""\n\nОпредели, просит ли пользователь напоминание.`
  );
}

/**
 * Голосовой/видео-вариант L2: заголовок + детект напоминания ОДНИМ вызовом. У голосовых на L1 нет текста
 * (он появляется только после STT), поэтому «напомни …» ловим здесь, по транскрипту — тем же вызовом,
 * что и заголовок (без доп. LLM-запроса).
 */
export const TITLE_REMIND_SYSTEM =
  'Ты придумываешь короткий заголовок сохранённой записи и определяешь, просит ли пользователь ' +
  'напомнить ему о ней. Контент может быть на любом языке. ' +
  'Отвечай строго JSON-объектом вида ' +
  '{"title":"<одна строка>","reminder":null|{"whenIso":"<ISO8601 со смещением пояса>"}}. ' +
  TITLE_RULE +
  ' ' +
  REMINDER_RULE;

/** Сигнал (транскрипт) + «сейчас» в поясе юзера + дефолтный час → заголовок + детект напоминания. */
export function titleRemindPrompt(signal: string, nowIso: string, defaultHour: number): string {
  return (
    `Сейчас (в поясе пользователя): ${nowIso}. Дефолтный час напоминания, если время не указано: ${defaultHour}:00.\n\n` +
    `Сигналы о контенте:\n"""\n${signal}\n"""\n\nПридумай заголовок и определи, просит ли пользователь напоминание.`
  );
}

/**
 * Парс ТОЛЬКО строки времени (намерение уже известно — юзер нажал «Напомнить»→«Своё»). Надёжнее
 * детекта, т.к. не надо различать «это напоминание или нет». Может вернуть один альтернативный вариант
 * (напр. «в 9» → 9:00 и 21:00) для инлайн-конфирма.
 */
export const PARSE_TIME_SYSTEM =
  'Пользователь вводит, КОГДА вернуть ему запись (намерение напомнить уже подтверждено). Преврати ' +
  'фразу в абсолютный момент. Отвечай строго JSON-объектом вида ' +
  '{"whenIso":"<ISO8601 со смещением|null>","ambiguous":<bool>,"altIso":"<ISO8601|null>"}. Поля:\n' +
  '• whenIso — наиболее вероятный момент В БУДУЩЕМ, ISO8601 со смещением пояса пользователя, ' +
  'относительно «сейчас» (дано в запросе). Если час не назван явно — дефолтный час (дан в запросе), минуты 00. ' +
  'Не смог понять время — null.\n' +
  '• ambiguous — true, если фраза допускает два разумных прочтения времени суток (напр. «в 9» = 9:00 или 21:00).\n' +
  '• altIso — при ambiguous: второй вариант (тот же день, другое время суток); иначе null.\n' +
  'Без пояснений, только JSON.';

export function parseTimePrompt(text: string, nowIso: string, defaultHour: number): string {
  return (
    `Сейчас (в поясе пользователя): ${nowIso}. Дефолтный час, если не указан: ${defaultHour}:00.\n\n` +
    `Фраза пользователя:\n"""\n${text}\n"""\n\nОпредели момент возврата.`
  );
}

/**
 * Vision-аннотация картинки одним вызовом: описание (в индекс, юзеру не показывается) + заголовок.
 * Категорий больше нет — генерируем description (для эмбеддинга/поиска) и title (показать юзеру).
 */
export const VISION_SYSTEM =
  'Ты описываешь сохранённую пользователем картинку для его «второго мозга». ' +
  'Отвечай строго JSON-объектом вида ' +
  '{"description":"...","title":"<одна строка>"}. Поля:\n' +
  '• "description" — 1–2 предложения на русском, фактично: что изображено и какой текст виден на ' +
  'картинке. Только факты с изображения, без оценок и догадок.\n' +
  '• "title" — суть записи своими словами одной строкой до 70 символов, ' +
  'без кавычек и точки в конце (напр. «Идея: оплата подписки через бота»).\n' +
  'Подсказки (подпись, OCR-текст, источник) могут быть шумными или не относиться к картинке — ' +
  'опирайся в первую очередь на само изображение.';

export function visionPrompt(ctx: { caption?: string; ocr?: string; source?: string }): string {
  const hints: string[] = [];
  if (ctx.caption?.trim()) hints.push(`Подпись пользователя: ${ctx.caption.trim()}`);
  if (ctx.ocr?.trim()) hints.push(`OCR-текст с картинки (возможно шумный): ${ctx.ocr.trim()}`);
  if (ctx.source?.trim()) hints.push(`Источник (канал/чат): ${ctx.source.trim()}`);
  const block = hints.length ? `Подсказки:\n"""\n${hints.join('\n')}\n"""\n\n` : '';
  return `${block}Опиши картинку и придумай заголовок.`;
}

